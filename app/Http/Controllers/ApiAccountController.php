    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $user = WebNote\User::find($id);
        $mail = $user->email;
        $name = $user->name;
        $avatar = $user->avatar;

        return Response::json([
            'name' => $name,
            'mail' => $mail,
            'avatar' => $avatar
        ]);
    }

